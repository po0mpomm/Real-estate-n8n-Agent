# Convert various area units -> square feet
UNIT_TO_SQFT = {
    "sq. meter": 10.7639, "square meter": 10.7639, "sq. meters": 10.7639,
    "sq. yard": 9.0, "square yards": 9.0, "sq. yards": 9.0,
    "acre": 43560.0, "acres": 43560.0,
    "perch": 272.25, "guntha": 1089.0, "ares": 1076.39,
    "cents": 435.6, "hectare": 107639.0, "ground": 2400.0, "grounds": 2400.0,
}

def parse_total_sqft(x: str) -> float:
    if pd.isna(x): return np.nan
    s = str(x).lower().strip()
    # ranges like "2100 - 2850"
    if "-" in s:
        parts = [p.strip() for p in s.split("-") if p.strip()]
        nums = []
        for p in parts:
            m = re.findall(r"[0-9]*\.?[0-9]+", p)
            if m: nums.append(float(m[0]))
        if len(nums) == 2:
            return float(np.mean(nums))
    # number + optional unit
    m = re.findall(r"([0-9]*\.?[0-9]+)", s)
    if m:
        val = float(m[0])
        unit = None
        for u in UNIT_TO_SQFT.keys():
            if u in s:
                unit = u
                break
        return val * UNIT_TO_SQFT[unit] if unit else val
    return np.nan

def extract_bhk(size: str) -> float:
    if pd.isna(size): return np.nan
    m = re.findall(r"(\d+)", str(size))
    return float(m[0]) if m else np.nan

def clean_locations(s: str) -> str:
    if pd.isna(s): return "other"
    return re.sub(r"\s+", " ", str(s).strip()).lower()

# Keep only useful columns (others optional)
keep_cols = ["area_type","availability","location","size","society","total_sqft","bath","balcony","price"]
