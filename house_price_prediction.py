# ---------- HYPERPARAMETER TUNING (use neg_mean_squared_error and compute RMSE) ----------
def grid_search_model(base_estimator, param_grid, X_train, y_train):
    pipe = Pipeline(steps=[("preprocessor", preprocessor), ("model", base_estimator)])
    gs = GridSearchCV(
        pipe,
        param_grid=param_grid,
        cv=5,
        scoring="neg_mean_squared_error",  # use MSE-based scoring for compatibility
        n_jobs=-1,
        verbose=1
    )
    gs.fit(X_train, y_train)
    return gs

ridge_params = {"model__alpha": [0.1, 1.0, 10.0, 25.0, 50.0, 100.0]}
rf_params = {
    "model__n_estimators": [200, 300],
    "model__max_depth": [None, 10, 15],
    "model__min_samples_split": [2, 5]
}

ridge_gs = grid_search_model(Ridge(), ridge_params, X_train, y_train)
rf_gs = grid_search_model(RandomForestRegressor(random_state=42), rf_params, X_train, y_train)

def report_gs(gs, X_test, y_test, name="Model"):
    preds = gs.predict(X_test)
    mae = mean_absolute_error(y_test, preds)
    mse = mean_squared_error(y_test, preds)
    rmse = float(np.sqrt(mse))
    r2 = r2_score(y_test, preds)
    print(f"\n{name} Best Params: {gs.best_params_}")
    print(f"{name} Test -> MAE: {mae:.3f} | RMSE: {rmse:.3f} | R2: {r2:.3f}")

report_gs(ridge_gs, X_test, y_test, "Ridge (GridSearch)")
report_gs(rf_gs, X_test, y_test, "RandomForest (GridSearch)")

