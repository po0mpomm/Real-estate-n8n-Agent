def trim_pps_outliers(d: pd.DataFrame) -> pd.DataFrame:
    m = d["price_per_sqft"].mean()
    s = d["price_per_sqft"].std()
    return d[(d["price_per_sqft"] > (m - s)) & (d["price_per_sqft"] <= (m + s))]

def bhk_sanity_filter(df_in: pd.DataFrame) -> pd.DataFrame:
    out = []
    for loc, sub in df_in.groupby("location_bucket"):
        means = sub.groupby("bhk")["price_per_sqft"].mean().to_dict()
        ok = sub[sub.apply(lambda r: r["price_per_sqft"] >= means.get(r["bhk"]-1, -np.inf), axis=1)]
        out.append(ok)
    return pd.concat(out, axis=0)

